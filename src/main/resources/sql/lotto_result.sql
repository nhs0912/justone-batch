                                `number2` INT NOT NULL,
                                `number3` INT NOT NULL,
                                `number4` INT NOT NULL,
                                `number5` INT NOT NULL,
                                `number6` INT NOT NULL,
                                `bonus_number` INT NOT NULL,
                                `rank1_winner_count` INT NOT NULL DEFAULT 0,
                                `rank1_winner_amount` DECIMAL(19,0) NOT NULL DEFAULT 0,
                                `rank1_sum_winner_amount` DECIMAL(19,0) NOT NULL DEFAULT 0,
                                `rank2_winner_count` INT NOT NULL DEFAULT 0,
                                `rank2_winner_amount` DECIMAL(19,0) NOT NULL DEFAULT 0,
                                `rank2_sum_winner_amount` DECIMAL(19,0) NOT NULL DEFAULT 0,
                                `rank3_winner_count` INT NOT NULL DEFAULT 0,
